	<script type="text/javascript">
	$(document).ready(function (){
		//小图片左右滚动
		var $slider = $('.L-Case-C ul');
		var $slider_child_l = $('.L-Case-C ul li').length;
		var $slider_width = $('.L-Case-C ul li').width()+15;
		$slider.width($slider_child_l * $slider_width);
		
		var slider_count = 0;
		
		if ($slider_child_l < 4) {
			$('#solution-right').css({cursor: 'auto'});
			$('#solution-right').removeClass("dasabled");
		}
		
		$('.L-but2').click(function() {
			if ($slider_child_l < 4 || slider_count >= $slider_child_l - 4) {
				return false;
			}
			
			slider_count++;
			$slider.animate({left: '-=' + $slider_width + 'px'}, 'fast');
			slider_pic();
		});
		
		$('.L-but1').click(function() {
			if (slider_count <= 0) {
				return false;
			}
			slider_count--;
			$slider.animate({left: '+=' + $slider_width + 'px'}, 'fast');
			slider_pic();
		});
		
		function slider_pic() {
			if (slider_count >= $slider_child_l - 4) {
				$('#solution-right').css({cursor: 'auto'});
				$('#solution-right').addClass("dasabled");
			}
			else if (slider_count > 0 && slider_count <= $slider_child_l - 4) {
				$('#solution-left').css({cursor: 'pointer'});
				$('#solution-left').removeClass("dasabled");
				$('#solution-right').css({cursor: 'pointer'});
				$('#solution-right').removeClass("dasabled");
			}
			else if (slider_count <= 0) {
				$('#solution-left').css({cursor: 'auto'});
				$('#solution-left').addClass("dasabled");
			}
		}
		
	});
	</script>
